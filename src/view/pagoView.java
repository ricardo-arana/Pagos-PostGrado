    private void llenarComboCiclo() {
        cbCiclo.addItem("Selecione Ciclo");
        MatriculaJpaController Mjc = new MatriculaJpaController(entityMan.getInstance());
        List<Matricula> listaCiclos = Mjc.findByEstudiante(e);
        for (Matricula m : listaCiclos) {
            cbCiclo.addItem(m.getCicloidCiclo());
        }
    }

    private String TipoPAgo(String idCuto) {
        String Desc = null;
        switch (idCuto) {
            case "0":
                Desc = "Matricua";
                break;
            case "1":
                Desc = "1ra Cuota";
                break;
            case "2":
                Desc = "2da Cuota";
                break;
            case "3":
                Desc = "3ra Cuota";
                break;
            case "4":
                Desc = "4ta Cuota";
                break;
        }
        return Desc;
    }

    private float obtenerSaldo(Pago p) {
        float saldo = 0;
        if (p.getCuota().equals("0")) {
            saldo = 200 - p.getMonto();
        } else {
            saldo = 300 - p.getMonto();
        }

        return saldo;

    }

    private String obenterMsg(Pago p) {
        String msg = "";
        if (obtenerSaldo(p) == 0) {
            msg = "completo";
        } else {
            msg = "incompleto";
        }

        return null;
    }
