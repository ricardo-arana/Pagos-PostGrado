    private void cbCicloItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbCicloItemStateChanged
        // TODO add your handling code here:
        PagoJpaController pjc = new PagoJpaController(entityMan.getInstance());
        float montoAcumulado = 0;
        
        if (cbCiclo.getSelectedIndex() != 0) {
            c = (Ciclo) cbCiclo.getSelectedItem();
            deM.setRowCount(0);
           
            List<Pago> lista = pjc.pagosFiltro(e, e.getMaestria(), c);

            for (Pago p : lista) {
                Object[] fila = {TipoPAgo(p.getCuota()), p.getMonto(), p.getMontoSaldo(), obenterMsg(p)};
                
                 montoAcumulado += p.getMonto();
                 ultimaCouta = p.getCuota();
                deM.addRow(fila);
            }
        } else {
            deM.setRowCount(0);
        }
        
        if(montoAcumulado<200 && montoAcumulado>=0){
             txtTipoPago.setText("Matricula");
             ultimaCouta = "0";
             montoMax = 200-montoAcumulado;
        }else if(montoAcumulado >= 200 && montoAcumulado <500){
            txtTipoPago.setText("1ra Cuota");
            ultimaCouta = "1";
            montoMax = 500-montoAcumulado;
        }else if(montoAcumulado >= 500 && montoAcumulado < 800){
            txtTipoPago.setText("2da Cuota");
            ultimaCouta = "2";
            montoMax = 800-montoAcumulado;
        }else if(montoAcumulado >=800 && montoAcumulado < 1100){
            txtTipoPago.setText("3ra Cuota");
            ultimaCouta ="3";
            montoMax = 1100-montoAcumulado;
        }else if(montoAcumulado >=1100 && montoAcumulado < 1400){
            txtTipoPago.setText("4ta Couta");
            ultimaCouta="4"; 
            montoMax = 1400-montoAcumulado;
        }else if(montoAcumulado == 1400){
            txtTipoPago.setText("pago completo este ciclo");
        }
        
        txtDescMonto.setText("Monto Max. "+montoMax);

    }//GEN-LAST:event_cbCicloItemStateChanged

