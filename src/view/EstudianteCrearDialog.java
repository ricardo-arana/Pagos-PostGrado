    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        EstudianteJpaController Ejc = new EstudianteJpaController(entityMan.getInstance());
        Estudiante newEst = new Estudiante();
            
            newEst.setApellido(txtApellido.getText());
            newEst.setNombre(txtNombre.getText());
            newEst.setDireccion(txtDireccion.getText());
            newEst.setDni(txtDni.getText());
            newEst.setMaestria((Maestria) cbMaestrias.getSelectedItem());
            newEst.setTelefono(txtTelefono.getText());
            User user = new User(1);
            newEst.setUsuarioRegistro(user);
        if (modo == 'N') {
            newEst.setIdEstudiante(null);
            Ejc.create(newEst);
            JOptionPane.showMessageDialog(null, "Usuario Registrado");
            this.dispose();
        }else if (modo == 'M'){
            newEst.setIdEstudiante(e.getIdEstudiante());
            try {
                Ejc.edit(newEst);
            } catch (Exception ex) {
                Logger.getLogger(EstudianteCrearDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Usuario Modificado");
            this.dispose();
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

