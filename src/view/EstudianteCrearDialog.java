    private void LlenarComboMaestria() {
        MaestriaJpaController mJC = new MaestriaJpaController(entityMan.getInstance());
        List<Maestria> maestrias = mJC.findMaestriaEntities();

        for (Maestria m : maestrias) {
            cbMaestrias.addItem(m);
        }
    }
