    private void inittabla() {
        T = (DefaultTableModel)tablaciclo.getModel() ;
        T.setRowCount(0);
        CicloJpaController Cjc = new CicloJpaController(entityMan.getInstance());
        List<Ciclo> listaC = Cjc.findCicloEntities();
        
        for (Ciclo c : listaC) {
            Object object[] = {c.getIdCiclo(),c};
            T.addRow(object);            
        }
        
                
       
    }
    void botones(boolean B){
        txtBuscar.setText("");
        txtBuscar.setEnabled(B);
        btnGuardar.setEnabled(B);
        btnCancelar.setEnabled(B);
        btnBorrar.setEnabled(!B);
        btnModificar.setEnabled(!B);
        btnNuevo.setEnabled(!B);
    }
